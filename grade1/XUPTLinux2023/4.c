#include <stdio.h>
int main(void)
{
    char a=64&127;
    char b=64^127;
    char c=-64>>6;
    char ch=a+b-c;
    printf("a = %d b = %d c = %d\n",a,b,c);
    printf("ch = %d\n",ch);
    /*
    我只能想到ch本应该是128，但是char类型只有7个位是数据位，那么范围就是[-128,127]。
    64的二进制补码是    0100 0000
    127的是             0111 1111
    那么64&127，与运算就是0100 0000，十进制就是64
    64^127，异或运算出来就是0011 1111，十进制就是63
    -64的二进制补码是1100 0000
    >>是向右移动，空出的位补符号位，-64符号位是1，那么移位后就是1111 1111
    所以十进制的话c=-1
    */
}